package org.sda.task10;

public class MoveDirection {
    private double xMD, yMD;

    public MoveDirection(double xMD, double yMD) {
        this.xMD = xMD;
        this.yMD = yMD;
    }

    public double getxMD() {
        return xMD;
    }

    public void setxMD(double xMD) {
        this.xMD = xMD;
    }

    public double getyMD() {
        return yMD;
    }

    public void setyMD(double yMD) {
        this.yMD = yMD;
    }
}
